/* 1 задача: Написать процедуру, которая выводит пользователей (имя/фамилия, пол, почтовый адрес и дату рождения), 
у которых день рождение в N квартале года. Сортировку провести по полу и дню и месяцу дня рождения.*/

DROP PROCEDURE IF EXISTS project_video.hp;

DELIMITER $$
$$
CREATE PROCEDURE project_video.hp(in number_quarter INT)
BEGIN
	SELECT 
	CONCAT(firstname, ' ', lastname),
	email,
	gender,
	birthday 
FROM users
WHERE QUARTER(birthday) = number_quarter
ORDER BY gender, DAYOFYEAR(birthday);
END$$
DELIMITER ;

-- вызов процедуры: нахожу пользователей с днем рождением во втором квартале года.
