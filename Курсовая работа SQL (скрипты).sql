/*Общее текстовое описание БД и решаемых ею задач.
 Цель проекта - создание модель БД для хранения и анализа данных, получаемых с видео-хостинга (аналогичному YouTube).
 Основные данные, которые содержит данная БД - это информация о пользователях, каналах, альбомах, видеозаписях, 
 а также информация о их создании или изменении.
 
 Пользователи могут создавать канал, просматривать видео, выражать эмоции ("лайкать" видео), писать комментарии к видео, покупать
 премиум альбомы и подписываться на каналы.
 На каналах можно создавать альбомы для загрузки видео. Видео должно быть загружно в альбом. По эмоциям, комментариям и просмотрам 
 можно определять самые популярные видео, альбомы, каналы или пользователей.
 
 При удалении пользователя - удаляется вся информация, которая была с ним связана.*/

DROP DATABASE IF EXISTS Project_video;
CREATE DATABASE Project_video;
USE Project_video;

-- 1. Создаю пользователя: 
DROP TABLE IF EXISTS users;
CREATE TABLE users (
	id SERIAL PRIMARY KEY,
    firstname VARCHAR(128) NOT NULL,
    lastname VARCHAR(128) NOT NULL,
    gender CHAR(1) NOT NULL,
    birthday DATE,
    email VARCHAR(128) UNIQUE NOT NULL,
    password_hash VARCHAR(16) DEFAULT NULL,
    phone BIGINT UNSIGNED DEFAULT NULL,
    photo VARCHAR(255) DEFAULT NULL, -- сслыка на фото
    is_deleted bit default 0,
    INDEX users_firstname_lastname_idx(firstname, lastname)
);

-- 2. Создаю каналы: 
DROP TABLE IF EXISTS channels;
CREATE TABLE channels (
	id SERIAL PRIMARY KEY,
    name VARCHAR(255) UNIQUE NOT NULL,
    user_id BIGINT UNSIGNED NOT NULL, -- автор канала
    is_deleted bit default 0,
    created_at DATETIME DEFAULT NOW(),
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE INDEX index_of_name ON channels (name);

-- 3. Создаю доступ к альбомам: 
DROP TABLE IF EXISTS accesses;
CREATE TABLE accesses (
	id SERIAL PRIMARY KEY,
	status ENUM ('открытый', 'закрытый', 'по ссылке', 'премиум') NOT NULL,
	body TEXT NOT NULL-- подробное описание каждого типа доступа для пользователя
);

-- 4. Создаю альбомы на каналах:
DROP TABLE IF EXISTS albums;
CREATE TABLE albums(
	id SERIAL PRIMARY KEY,
    name_album VARCHAR(255) NOT NULL,
    channel_id BIGINT UNSIGNED NOT NULL,
    body TEXT,
    access_id BIGINT UNSIGNED NOT NULL,
    price INT DEFAULT 0,
    created_at DATETIME DEFAULT NOW(),
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    is_deleted BIT DEFAULT 0,
    
FOREIGN KEY (channel_id) REFERENCES channels(id) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (access_id) REFERENCES accesses(id) ON UPDATE CASCADE ON DELETE RESTRICT
);

-- 5. Создаю видео:
DROP TABLE IF EXISTS video;
CREATE TABLE video(
	id SERIAL PRIMARY KEY,
    name_video VARCHAR(255) NOT NULL,
    body TEXT, -- описание
    album_id BIGINT UNSIGNED NOT NULL,
	created_at DATETIME DEFAULT NOW(), -- дата загрузки видео
	published_at DATETIME NOT NULL, -- дата публикации видео
    is_deleted BIT DEFAULT 0,

    FOREIGN KEY (album_id) REFERENCES albums(id) ON UPDATE CASCADE ON DELETE CASCADE,
    INDEX video_name_video_idx(name_video)
);

-- 6. Создаю истории просмотров:
DROP TABLE IF EXISTS history;
CREATE TABLE history(
	id SERIAL PRIMARY KEY,
    video_id BIGINT UNSIGNED,
    user_id BIGINT UNSIGNED, -- кто просмотрел
	viewed_at DATETIME DEFAULT NOW(), -- дата просмотра
    
FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (video_id) REFERENCES video(id) ON UPDATE CASCADE ON DELETE CASCADE
);

-- 7. Создаю лайки/дейлайки/нет эмоций:
DROP TABLE IF EXISTS likes;
CREATE TABLE likes(
    user_id BIGINT UNSIGNED,
    video_id BIGINT UNSIGNED NOT NULL,
	status ENUM ('лайк', 'дезлайк', 'нет эмоций') DEFAULT 'нет эмоций',
	created_at DATETIME DEFAULT NOW(),
	updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	
PRIMARY KEY (user_id, video_id),
FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (video_id) REFERENCES video(id) ON UPDATE CASCADE ON DELETE CASCADE
);

-- 8. Создаю комментарии:
DROP TABLE IF EXISTS comments;
CREATE TABLE comments(
	id SERIAL PRIMARY KEY,
    user_id BIGINT UNSIGNED,
    video_id BIGINT UNSIGNED NOT NULL,
    body TEXT NOT NULL,
	created_at DATETIME DEFAULT NOW(),
	
FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (video_id) REFERENCES video(id) ON UPDATE CASCADE ON DELETE CASCADE
);

-- 9. Создаю премиум альбомы:
DROP TABLE IF EXISTS premium;
CREATE TABLE premium(
	id SERIAL PRIMARY KEY,
    user_id BIGINT UNSIGNED NOT NULL, -- пользователь, купивший премиум
    album_id BIGINT UNSIGNED NOT NULL, -- альбом, имеющий статус премиум
    start_date DATETIME DEFAULT NOW(),
    expiration_date DATETIME,
    price INT NOT NULL,
    
FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (album_id) REFERENCES albums(id) ON UPDATE CASCADE ON DELETE CASCADE
);

-- 10. Создаю подписчиков каналов:
DROP TABLE IF EXISTS subscribers;
CREATE TABLE subscribers(
	id SERIAL PRIMARY KEY,
	channel_id BIGINT UNSIGNED NOT NULL,
	user_id BIGINT UNSIGNED NOT NULL, -- подписчик канала
	created_at DATETIME DEFAULT NOW(), -- дата события
    is_sub bit default 1, -- 0 - пользователь не подписан, 1 - пользователь подписан
	
FOREIGN KEY (channel_id) REFERENCES channels(id) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE
);


