-- Представления:
/* 1 задача: Представление, выводящее информацию по видео: название видео, кол-во просмотров, кол-во комментариев, название альбома 
и название канала (где расположено видео). Сортировка видео по уменьшению кол-ва просмотров*/

CREATE OR REPLACE VIEW v_video
AS

 SELECT
	v.id, 
	v.name_video as 'Название видео',
	h.his as cnt, -- Кол-во просмотров
	l.lik as 'Кол-во лайков',
	c.comm as 'Кол-во комм',
	a.name_album as 'Название альбома',
	ch.name as 'Название канала'
FROM video v 
	LEFT JOIN (SELECT COUNT(id) as his, video_id FROM history GROUP BY video_id) as h ON v.id = h.video_id
	LEFT JOIN (SELECT COUNT(video_id) as lik, video_id FROM likes GROUP BY video_id) as l ON v.id = l.video_id
	LEFT JOIN (SELECT COUNT(id) as comm, video_id FROM comments GROUP BY video_id) as c ON v.id = c.video_id
	LEFT JOIN albums a ON v.album_id = a.id
	LEFT JOIN channels as ch ON a.channel_id = ch.id 

GROUP BY v.id
ORDER BY cnt DESC;

