SELECT 
	users.id,
	CONCAT(users.firstname, ' ', users.lastname) as 'Пользователь',
	users.email,
	(SELECT COUNT(id) FROM subscribers WHERE user_id = users.id) AS 'Каналы', -- кол-во каналов, на которые подписан пользователь
	(SELECT COUNT(id) FROM comments WHERE user_id = users.id) AS 'Комментарии', -- кол-во комментариев, которые оставил пользователь
	(SELECT COUNT(id) FROM history WHERE user_id = users.id) AS 'Кол-во просмотров', -- общее количество просмотров пользователя
	(SELECT COUNT(user_id) FROM likes WHERE user_id = users.id) AS 'Кол-во лайков' -- кол-во лайков, которые поставил пользователь
FROM
	users
WHERE is_deleted != 0
ORDER BY users.id;


