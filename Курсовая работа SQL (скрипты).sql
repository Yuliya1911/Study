/* 2 задача: Написать процедуру, которая выводит владельца премиум альбома, название альбома, стоимость, названия видео в 
данном альбоме и дату их публикации в зависимости от стоимости альбома. */

DROP PROCEDURE IF EXISTS project_video.hp_albums_premium;

DELIMITER $$
$$
CREATE PROCEDURE project_video.hp_albums_premium(IN fix INT)
BEGIN
	SELECT
		CONCAT(u.firstname, ' ', u.lastname) as 'Владелец альбома',
		a.name_album,
		a.price,
		v.name_video,
		v.published_at 
	FROM albums as a 
		JOIN channels as c ON a.channel_id = c.id 
		JOIN users as u ON c.user_id = u.id 
		JOIN video as v ON a.id = v.album_id
WHERE access_id = (SELECT id FROM accesses where status = 'премиум') AND a.price = fix
	ORDER BY c.name, a.name_album;
END$$
DELIMITER ;

-- вызов процедуры: нахожу владельцев премиум альбомов в зависимости от стоимости (варьируется от 1 до 15).
CALL project_video.hp_albums_premium(2);
