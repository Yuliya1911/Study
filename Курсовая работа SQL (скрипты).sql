use project_video;

SELECT 
	CASE (u.gender)
         WHEN 'm' THEN 'Мужской'
         WHEN 'f' THEN 'Женский'
         ELSE 'другой'
    END AS 'Пол', 
	ROUND(AVG(TIMESTAMPDIFF(YEAR, u.birthday, NOW())), 0) as 'Возраст',
	COUNT(u.id) AS 'Кол-во пользователей'
FROM 
	users AS u
GROUP BY u.gender, 'Возраст';

/* 2 задача: Вывести для каждого пользователя названия всех альбомов и количество видеозаписей в каждом альбоме (у которого имеется).
LEFT JOIN не писала специально, чтобы не было много записей. */

SELECT 
	CONCAT(u.firstname, ' ', u.lastname) as 'Пользователь',
	a.name_album as 'Название альбома',
	COUNT(v.id) as 'Кол-во видео'
FROM users AS u 
	JOIN channels AS c ON u.id = c.user_id 
	JOIN albums AS a ON c.id = a.channel_id 
	JOIN video AS v ON a.id = v.album_id
GROUP BY u.id, a.name_album  
ORDER BY CONCAT(u.firstname, ' ', u.lastname);
	

-- JOIN'ы:
-- 1 задача: Вывести id, имя/фамилию 10-и пользователей, которые подписаны на 5 и более каналов.

SELECT
	u.id,	
	CONCAT(u.firstname, ' ', u.lastname) as 'Имя пользователя',
	COUNT(s.id) AS 'Кол-во каналов'
FROM subscribers AS s
	JOIN users AS u ON s.user_id = u.id 
GROUP BY s.user_id HAVING COUNT(s.id) >= 5
ORDER BY COUNT(s.id) DESC
LIMIT 10; -- выводит 4-х пользователей, т.к. у всех остальных подписка оформлена на 3-и и менее каналов.

-- 2 задача: Составить список всех каналов и их владельцев, на которых было куплено более 2-х премиум альбомов.

SELECT 
	u.firstname as 'Владелец канала',
	c.name as 'Название канала', 
	COUNT(a.name_album) as cnt -- количество премиум альбомов на канале
FROM users AS u 
	JOIN channels AS c ON u.id = c.user_id
	JOIN albums AS a ON c.id = a.channel_id
	JOIN accesses AS ac ON a.access_id = ac.id WHERE ac.status = 'премиум'
GROUP BY c.id 
HAVING cnt > 2 ORDER BY cnt DESC; -- выводит 2 канала из 5

