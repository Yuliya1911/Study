и владельца альбома.*/
CREATE OR REPLACE VIEW v_albums
AS

SELECT 
	a.id, 
	a.name_album as 'Название альбома',
	v.name_video as 'Название видео',
	v.published_at, -- дата публикации видео
	u.firstname as 'Пользователь' -- который опубликовал видео (он же владелец канала)
FROM albums as a
	LEFT JOIN video as v ON a.id = v.album_id 
	LEFT JOIN channels as c ON a.channel_id = c.id
	LEFT JOIN users as u ON c.user_id = u.id
WHERE access_id = (SELECT id FROM accesses where status = 'открытый')
ORDER BY a.id, v.name_video;

